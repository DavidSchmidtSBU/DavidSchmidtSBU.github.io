package programmingassignment4;

public class TortoiseandHare
{
	public static void main(String []args)
	{
		int tortoise = 0;
		int hare = 0;
		
		
		if (tortoise <= 10)
		  System.out.println("winning");
		else if (tortoise >= 20)
		  System.out.println("losing");
		else if (tortoise == 30)
		  System.out.println("tied");
		else if (hare <= 40)
			System.out.println("winning");
		else if (hare >= 50)
		  System.out.println("losing");
		else if (hare == 60)
		  System.out.println("tied");
		
	}//main
}//class
